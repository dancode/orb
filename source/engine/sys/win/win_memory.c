usize
sys_page_size( void )
{
    SYSTEM_INFO si;
    GetSystemInfo( &si );
    return ( usize )si.dwPageSize;
}

/*==============================================================================================
    Unity build
==============================================================================================*/

usize
sys_large_page_size( void )
{
    return ( usize )GetLargePageMinimum(); /* 0 if not supported */
}

