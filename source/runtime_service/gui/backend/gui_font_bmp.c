/*==============================================================================================

    runtime_service/gui/backend/gui_font_bmp.c -- bmp fonts: monospace R8 grid atlases.

    A "bmp font" is an 8-bit-per-pixel (R8) monospace grid atlas sampled at draw time.  The
    built-in fonts ship as 1-bit-per-pixel "bit fonts" (the generated font/*.c tables, described
    by bit_font_def_t); bmp_atlas_build() *expands* each bit font into a bmp atlas at init.  The
    same atlas could later be produced from an externally authored bmp source without being a
    compile-time built-in -- nothing here assumes the pixels came from a bit font once built.

    Atlas layout (bit font -> bmp, same formula for every built-in):
        16 glyphs per row x glyph_w pixels = atlas_w pixels wide
        N  glyph rows     x glyph_h pixels = atlas_h pixels tall
    Glyph index for character ch (32..127): idx = ch - 32; col = idx % 16, row = idx / 16.

    Every atlas is finalized by font_finalize_atlas() (gui_font.c): a white texel row plus the
    dash pattern rows are appended, and the white / dash / scale-free UV metrics are resolved.
    Only the scale-dependent metrics (char_h/line_h/char_w/size) are re-resolved per selection.

    Included by gui_backend.c after gui_font.h, before gui_font.c.

==============================================================================================*/
// clang-format off

