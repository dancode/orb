/*----------------------------------------------------------------------------------------------
    bmp_font_t -- a bit font expanded into a resident runtime R8 atlas.
----------------------------------------------------------------------------------------------*/

typedef struct
{
    font_metrics_t              metrics;   // first: resolved metrics for this atlas
    const bit_font_def_t*       def;       // source bit font (grid + 1-bpp data)
    rhi_texture_t               atlas;     // GPU atlas
    u32                         atlas_idx; // bindless index
    u32                         tex_h;     // uploaded atlas height (atlas_h + white + dash rows)

} bmp_font_t;

/* One slot per gui_font_t; designated initializers keep slot == enum value, so bmp_select()
   and the init/shutdown loops can index directly. */
static bmp_font_t s_bmps[ GUI_FONT_BITMAP_MAX ] =
{
    // built-in fonts generated by AI
    [ GUI_FONT_BITMAP_8 ]           = { .def = &s_def_8 },
    [ GUI_FONT_BITMAP_16 ]          = { .def = &s_def_16 }, 

    // font_tool.exe generated
    [ GUI_FONT_BITMAP_16_JETBOLD]   = { .def = &s_def_jetbrains_bold_22 }, // fixed
    [ GUI_FONT_BITMAP_20_JETBOLD]   = { .def = &s_def_jetbrains_bold_27 }, // fixed
    [ GUI_FONT_BITMAP_24_JETBOLD ]  = { .def = &s_def_jetbrains_bold_33 }, // fixed

    [ GUI_FONT_BITMAP_24_CONSOLA ]  = { .def = &s_def_consola_24 },
};

static bmp_font_t*      s_bmp_active   = NULL;
static gui_font_t     s_bmp_font     = GUI_FONT_BITMAP_16;
static u32              s_bmp_scale    = 1;

