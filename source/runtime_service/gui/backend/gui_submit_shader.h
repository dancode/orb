/*==============================================================================================

    runtime_service/gui/backend/gui_submit_shader.h -- Embedded SPIR-V for the gui pipeline.

    Compiled from shaders/gui.vert and shaders/gui.frag using:
        glslc --target-env=vulkan1.3 gui.vert -o gui.vert.spv
        glslc --target-env=vulkan1.3 gui.frag -o gui.frag.spv

    Push constant block (shared by vert + frag, 80 bytes):
        mat4 mvp; uint tex_idx; uint samp_idx; uint dbg_flat; uint dbg_tint;

    dbg_flat / dbg_tint drive the debug render views (normal rendering leaves both 0):
        dbg_flat != 0 -- bypass the atlas and output a flat color, so geometry is visible
                         regardless of glyph coverage (the wireframe LINE pipeline, and the
                         per-batch tint view both set this).
        dbg_tint != 0 -- packed RGBA8 batch tint used as that flat color (R = low byte); 0
                         falls back to the vertex color (what the wireframe view uses).

    Vertex GLSL (shaders/gui.vert):
        version 450
        push_constant PC { mat4 mvp; uint tex_idx; uint samp_idx; uint dbg_flat; uint dbg_tint; } pc;
        location 0 in  vec2 in_pos;
        location 1 in  vec2 in_uv;
        location 2 in  vec4 in_color;   // UNORM4 byte RGBA -> normalized float4
        location 0 out vec4 v_color;
        location 1 out vec2 v_uv;
        main: gl_Position = pc.mvp * vec4(in_pos, 0, 1); v_color=in_color; v_uv=in_uv;

    Fragment GLSL (shaders/gui.frag):
        version 450 + GL_EXT_nonuniform_qualifier
        set=0 binding=0 texture2D u_textures[];
        set=0 binding=1 sampler   u_samplers[];
        push_constant PC { mat4 mvp; uint tex_idx; uint samp_idx; uint dbg_flat; uint dbg_tint; } pc;
        location 0 in  vec4 v_color;
        location 1 in  vec2 v_uv;
        location 0 out vec4 out_color;
        main: if (dbg_flat) out_color = flat(dbg_tint ? dbg_tint : v_color); else
              { vec4 s = texture(sampler2D(u_textures[tex_idx], u_samplers[samp_idx]), v_uv);
                out_color = vec4(srgb_to_linear(v_color.rgb), v_color.a * s.r); }

        Note: the swapchain is a _SRGB format, so colors are authored in sRGB and
        decoded to linear here -- the GPU then blends in linear space and re-encodes
        on store (the standard game-engine color path).  s.r is 1.0 for the white-pixel
        tex (solid color); the R channel of the R8_UNORM font atlas acts as per-glyph
        alpha, multiplied by the vertex color alpha.

==============================================================================================*/

