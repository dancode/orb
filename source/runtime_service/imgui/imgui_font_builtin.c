/*==============================================================================================

    runtime_service/imgui/imgui_font_builtin.c -- Built-in bitmap font atlas data.

    Two hardcoded bitmap fonts serve as the compile-time fallback (no file required):
        8x8  -- compact atlas 128x64,  pixel-perfect at multiples of 8
        8x12 -- default atlas 128x128, pixel-perfect at multiples of 12

    Atlas layout (same formula for both):
        16 glyphs per row x glyph_w pixels = atlas_w pixels wide
        6 glyph rows      x glyph_h pixels = atlas_h pixels tall
    Glyph index for character ch (32-127): idx = ch - 32
        col = idx % 16,  row = idx / 16
    UV: u0 = col*glyph_w/atlas_w,  v0 = row*glyph_h/atlas_h

    Included by imgui.c before imgui_font.c.

==============================================================================================*/
// clang-format off

/*----------------------------------------------------------------------------------------------
    Types
----------------------------------------------------------------------------------------------*/

/* Pre-resolved metrics shared by both font sources; callers read from s_font. */
typedef struct
{
    f32  char_h;        // pixel height of the text area
    f32  line_h;        // total line advance
    f32  char_w;        // monospace advance (0 for proportional fonts)
    u32  atlas_idx;     // bindless texture index
    bool proportional;  // true = use per-glyph advance from lookup[]

} font_metrics_t;

static font_metrics_t* s_font = NULL;  // points to the active font's metrics

/* Compile-time description of one bitmap font. */
typedef struct
{
    u32         atlas_w;            // width of the font atlas in pixels
    u32         atlas_h;            // height of the font atlas in pixels
    u32         glyph_w;            // width of each glyph cell in pixels (including padding; 0 for whitespace)
    u32         glyph_h;            // height of each glyph cell in pixels (including padding)

    u32         glyphs_row;         // number of glyphs per row in the atlas (16 for our built-in fonts)
    u32         glyph_count;        // number of glyphs in the atlas (96 for our built-in fonts; 0 for whitespace-only)

    const u8*   data;    
    const char* debug_name;

} bitmap_font_def_t;

/* Runtime GPU resources for one bitmap font. */
typedef struct
{
    font_metrics_t           metrics;   // first: common resolved values (char_h, char_w, etc.)

    const bitmap_font_def_t* def;
    rhi_texture_t            atlas;
    u32                      atlas_idx;

} bitmap_font_t;

