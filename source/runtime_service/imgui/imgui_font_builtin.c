static const bitmap_font_def_t s_def_12 = {
    .atlas_w     = 128,
    .atlas_h     = 128,
    .glyph_w     = 8,
    .glyph_h     = 12,
    .glyphs_row  = 16,
    .data        = &s_font12_data[ 0 ][ 0 ],
    .glyph_count = 96,
    .debug_name  = "imgui_builtin_12",
};

/*----------------------------------------------------------------------------------------------
    GPU atlas instances
----------------------------------------------------------------------------------------------*/

static bitmap_font_t  s_bitmap_8      = { .def = &s_def_8  };
static bitmap_font_t  s_bitmap_12     = { .def = &s_def_12 };
static bitmap_font_t* s_bitmap_active = NULL;

static imgui_font_t s_bmp_font  = IMGUI_FONT_BITMAP_12;
static u32          s_bmp_scale = 1;

static void
bitmap_font_select( imgui_font_t font )
{
    s_bmp_font      = font;
    s_bitmap_active = ( font == IMGUI_FONT_BITMAP_12 ) ? &s_bitmap_12 : &s_bitmap_8;

    /* Resolve metrics from native pixel dimensions scaled by s_bmp_scale.
       All downstream code (widget sizing, glyph quads, text width) reads from s_font,
       so the scale is transparent -- callers see it as if the font were natively that size. */
    const bitmap_font_def_t* def = s_bitmap_active->def;
    f32 s = (f32)s_bmp_scale;
    s_bitmap_active->metrics = ( font_metrics_t ){
        .char_h       = (f32)def->glyph_h * s,
        .line_h       = (f32)( def->glyph_h + def->glyph_h / 4u ) * s,
        .char_w       = (f32)def->glyph_w * s,
        .atlas_idx    = s_bitmap_active->atlas_idx,
        .proportional = false,
    };
    s_font = &s_bitmap_active->metrics;
}

static void
bitmap_scale_set( u32 scale )
{
    s_bmp_scale = ( scale < 1u ) ? 1u : scale;
    if ( s_bitmap_active )
        bitmap_font_select( s_bmp_font );   // reapply metrics with new scale
}

/*----------------------------------------------------------------------------------------------
    bitmap_atlas_shutdown / bitmap_atlas_init
----------------------------------------------------------------------------------------------*/

static void
bitmap_atlas_shutdown( bitmap_font_t* bf )
{
    if ( bf->atlas_idx != 0 )
    {
        rhi()->unregister_texture( bf->atlas_idx );
        bf->atlas_idx = 0;
    }
    if ( rhi_handle_valid( bf->atlas ) )
    {
        rhi()->texture_destroy( bf->atlas );
        bf->atlas = ( rhi_texture_t ){ 0 };
    }
}

static bool
bitmap_atlas_init( bitmap_font_t* bf )
{
    const bitmap_font_def_t* def = bf->def;

    u8 pixels[ 128 * 128 ] = { 0 };

    for ( u32 g = 0; g < def->glyph_count; ++g )
    {
        u32 col = g % def->glyphs_row;
        u32 row = g / def->glyphs_row;
        u32 ox  = col * def->glyph_w;
        u32 oy  = row * def->glyph_h;

        for ( u32 r = 0; r < def->glyph_h; ++r )
        {
            u8 bits = def->data[ g * def->glyph_h + r ];
            for ( u32 c = 0; c < def->glyph_w; ++c )
            {
                u32 px = ox + c;
                u32 py = oy + r;
                pixels[ py * def->atlas_w + px ] = ( bits & ( 1u << c ) ) ? 0xFF : 0x00;
            }
        }
    }

    bf->atlas = rhi()->texture_create( &( rhi_texture_desc_t ){
        .width        = def->atlas_w,
        .height       = def->atlas_h,
        .depth        = 1,
        .mip_levels   = 1,
        .array_layers = 1,
        .format       = RHI_FORMAT_R8_UNORM,
        .usage        = RHI_TEXTURE_USAGE_SAMPLED | RHI_TEXTURE_USAGE_TRANSFER_DST,
        .memory       = RHI_MEMORY_GPU_ONLY,
        .debug_name   = def->debug_name,
    } );
    if ( !rhi_handle_valid( bf->atlas ) )
        return false;

    if ( !rhi()->upload_texture( bf->atlas, pixels, def->atlas_w * def->atlas_h, 0, 0 ) )
    {
        rhi()->texture_destroy( bf->atlas );
        bf->atlas = ( rhi_texture_t ){ 0 };
        return false;
    }

    bf->atlas_idx = rhi()->register_texture( bf->atlas );
    if ( bf->atlas_idx == 0 )
    {
        rhi()->texture_destroy( bf->atlas );
        bf->atlas = ( rhi_texture_t ){ 0 };
        return false;
    }

    return true;
}

