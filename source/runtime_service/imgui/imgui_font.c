/*----------------------------------------------------------------------------------------------
    GPU resources
----------------------------------------------------------------------------------------------*/

static rhi_texture_t s_atlas      = { 0 };
static u32           s_atlas_idx  = 0;

/*----------------------------------------------------------------------------------------------
    font_init -- rasterize bitmap data into an R8_UNORM texture and upload to GPU
----------------------------------------------------------------------------------------------*/

static bool
font_init( void )
{
    /* Build the pixel atlas in a stack buffer. */
    u8 pixels[ FONT_ATLAS_W * FONT_ATLAS_H ] = { 0 };

    for ( u32 g = 0; g < 96; ++g )
    {
        u32 col =   g % FONT_GLYPHS_ROW;
        u32 row =   g / FONT_GLYPHS_ROW;
        u32 ox  = col * FONT_GLYPH_W;
        u32 oy  = row * FONT_GLYPH_H;

        for ( u32 r = 0; r < FONT_GLYPH_H; ++r )
        {
            u8 bits = s_font_data[ g ][ r ];
            for ( u32 c = 0; c < FONT_GLYPH_W; ++c )
            {
                u32 px = ox + c;
                u32 py = oy + r;
                pixels[ py * FONT_ATLAS_W + px ] = ( bits & ( 1u << c ) ) ? 0xFF : 0x00;
            }
        }
    }

    s_atlas = rhi()->texture_create( &( rhi_texture_desc_t ){
        .width       = FONT_ATLAS_W,
        .height      = FONT_ATLAS_H,
        .depth       = 1,
        .mip_levels  = 1,
        .array_layers = 1,
        .format      = RHI_FORMAT_R8_UNORM,
        .usage       = RHI_TEXTURE_USAGE_SAMPLED | RHI_TEXTURE_USAGE_TRANSFER_DST,
        .memory      = RHI_MEMORY_GPU_ONLY,
        .debug_name  = "imgui_font_atlas",
    } );
    if ( !rhi_handle_valid( s_atlas ) )
        return false;

    if ( !rhi()->upload_texture( s_atlas, pixels, sizeof( pixels ), 0, 0 ) )
    {
        rhi()->texture_destroy( s_atlas );
        s_atlas = ( rhi_texture_t ){ 0 };
        return false;
    }

    s_atlas_idx = rhi()->register_texture( s_atlas );
    if ( s_atlas_idx == 0 )
    {
        rhi()->texture_destroy( s_atlas );
        s_atlas = ( rhi_texture_t ){ 0 };
        return false;
    }

    return true;
}

/*----------------------------------------------------------------------------------------------
    font_shutdown
----------------------------------------------------------------------------------------------*/

static void
font_shutdown( void )
{
    if ( s_atlas_idx != 0 )
    {
        rhi()->unregister_texture( s_atlas_idx );
        s_atlas_idx = 0;
    }
    if ( rhi_handle_valid( s_atlas ) )
    {
        rhi()->texture_destroy( s_atlas );
        s_atlas = ( rhi_texture_t ){ 0 };
    }
}

/*----------------------------------------------------------------------------------------------
    font_glyph -- UV rect in the atlas for a character
----------------------------------------------------------------------------------------------*/

static void
font_glyph( u8 ch, f32* u0, f32* v0, f32* u1, f32* v1 )
{
    if ( ch < 32 || ch > 127 )
        ch = '?';

    u32 idx = (u32)( ch - 32 );
    u32 col = idx % FONT_GLYPHS_ROW;
    u32 row = idx / FONT_GLYPHS_ROW;

    *u0 = (f32)( col * FONT_GLYPH_W ) / (f32)FONT_ATLAS_W;
    *v0 = (f32)( row * FONT_GLYPH_H ) / (f32)FONT_ATLAS_H;
    *u1 = *u0 + (f32)FONT_GLYPH_W / (f32)FONT_ATLAS_W;
    *v1 = *v0 + (f32)FONT_GLYPH_H / (f32)FONT_ATLAS_H;
}

static f32 font_char_w( void ) { return (f32)s_layout.font_size; }
static f32 font_char_h( void ) { return (f32)s_layout.font_size; }
static f32 font_line_h( void ) { return (f32)( s_layout.font_size + s_layout.font_size / 4u ); }

/* Approximate pixel width of a string (monospace). */
static f32
font_text_w( const char* str )
{
    f32 w = 0.0f;
    for ( ; *str; ++str )
        w += font_char_w();
    return w;
}

