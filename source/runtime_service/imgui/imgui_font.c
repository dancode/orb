/*----------------------------------------------------------------------------------------------
    GPU atlas instances  (bitmap fallback)
----------------------------------------------------------------------------------------------*/

static font_atlas_t  s_font_8      = { .def = &s_font_def_8  };
static font_atlas_t  s_font_12     = { .def = &s_font_def_12 };
static font_atlas_t* s_font_active = NULL;

static void
font_select( u32 font_size )
{
    s_font_active = ( font_size >= 12u ) ? &s_font_12 : &s_font_8;
}

/*----------------------------------------------------------------------------------------------
    Loaded TrueType font state  (s_orb)

    When s_orb.loaded is true all font_* helpers use s_orb instead of s_font_active.
    lookup[] is indexed by (codepoint - 32); entries with advance == 0 are missing glyphs.
----------------------------------------------------------------------------------------------*/

static struct
{
    orb_font_glyph_t  lookup[ 95 ];    /* codepoints 32..126 */
    i32               ascent;
    i32               descent;
    u32               atlas_w;
    u32               atlas_h;
    rhi_texture_t     atlas;
    u32               atlas_idx;
    bool              loaded;
} s_orb;

/*----------------------------------------------------------------------------------------------
    atlas_shutdown_one / atlas_init_one  (bitmap path)
----------------------------------------------------------------------------------------------*/

static void
atlas_shutdown_one( font_atlas_t* fa )
{
    if ( fa->atlas_idx != 0 )
    {
        rhi()->unregister_texture( fa->atlas_idx );
        fa->atlas_idx = 0;
    }
    if ( rhi_handle_valid( fa->atlas ) )
    {
        rhi()->texture_destroy( fa->atlas );
        fa->atlas = ( rhi_texture_t ){ 0 };
    }
}

static bool
atlas_init_one( font_atlas_t* fa )
{
    const font_def_t* def = fa->def;

    u8 pixels[ 128 * 128 ] = { 0 };

    for ( u32 g = 0; g < def->glyph_count; ++g )
    {
        u32 col = g % def->glyphs_row;
        u32 row = g / def->glyphs_row;
        u32 ox  = col * def->glyph_w;
        u32 oy  = row * def->glyph_h;

        for ( u32 r = 0; r < def->glyph_h; ++r )
        {
            u8 bits = def->data[ g * def->glyph_h + r ];
            for ( u32 c = 0; c < def->glyph_w; ++c )
            {
                u32 px = ox + c;
                u32 py = oy + r;
                pixels[ py * def->atlas_w + px ] = ( bits & ( 1u << c ) ) ? 0xFF : 0x00;
            }
        }
    }

    fa->atlas = rhi()->texture_create( &( rhi_texture_desc_t ) {
        .width        = def->atlas_w,
        .height       = def->atlas_h,
        .depth        = 1,
        .mip_levels   = 1,
        .array_layers = 1,
        .format       = RHI_FORMAT_R8_UNORM,
        .usage        = RHI_TEXTURE_USAGE_SAMPLED | RHI_TEXTURE_USAGE_TRANSFER_DST,
        .memory       = RHI_MEMORY_GPU_ONLY,
        .debug_name   = def->debug_name,
    } );
    if ( !rhi_handle_valid( fa->atlas ) )
        return false;

    if ( !rhi()->upload_texture( fa->atlas, pixels, def->atlas_w * def->atlas_h, 0, 0 ) )
    {
        rhi()->texture_destroy( fa->atlas );
        fa->atlas = ( rhi_texture_t ){ 0 };
        return false;
    }

    fa->atlas_idx = rhi()->register_texture( fa->atlas );
    if ( fa->atlas_idx == 0 )
    {
        rhi()->texture_destroy( fa->atlas );
        fa->atlas = ( rhi_texture_t ){ 0 };
        return false;
    }

    return true;
}

/*----------------------------------------------------------------------------------------------
    font_unload_orb / font_load_orb
----------------------------------------------------------------------------------------------*/

static void
font_unload_orb( void )
{
    if ( !s_orb.loaded )
        return;

    if ( s_orb.atlas_idx != 0 )
    {
        rhi()->unregister_texture( s_orb.atlas_idx );
        s_orb.atlas_idx = 0;
    }

    if ( rhi_handle_valid( s_orb.atlas ) )
    {
        rhi()->texture_destroy( s_orb.atlas );
        s_orb.atlas = ( rhi_texture_t ){ 0 };
    }
    s_orb.loaded = false;
}

static bool
font_load_orb( const char* path )
{
    font_unload_orb();

    FILE* f = fopen( path, "rb" );
    if ( !f )
        return false;

    /* Validate header. */
    orb_font_header_t hdr;
    if ( fread( &hdr, sizeof( hdr ), 1, f ) != 1
         || hdr.magic   != ORB_FONT_MAGIC
         || hdr.version != ORB_FONT_VERSION
         || hdr.glyph_count == 0 || hdr.glyph_count > 256
         || hdr.atlas_w == 0     || hdr.atlas_h == 0 )
    {
        fclose( f );
        return false;
    }

    /* Build lookup table from glyph records. */
    memset( s_orb.lookup, 0, sizeof( s_orb.lookup ) );
    for ( u32 i = 0; i < hdr.glyph_count; ++i )
    {
        orb_font_glyph_t g;
        if ( fread( &g, sizeof( g ), 1, f ) != 1 ) { fclose( f ); return false; }
        if ( g.codepoint >= 32 && g.codepoint <= 126 )
            s_orb.lookup[ g.codepoint - 32 ] = g;
    }

    /* Read pixel data and upload to GPU. */
    u32  pixel_count = hdr.atlas_w * hdr.atlas_h;
    u8*  pixels      = (u8*)malloc( pixel_count );
    if ( !pixels ) { fclose( f ); return false; }

    if ( fread( pixels, 1, pixel_count, f ) != pixel_count )
    {
        free( pixels );
        fclose( f );
        return false;
    }
    fclose( f );

    s_orb.atlas = rhi()->texture_create( &( rhi_texture_desc_t ) {
        .width        = hdr.atlas_w,
        .height       = hdr.atlas_h,
        .depth        = 1,
        .mip_levels   = 1,
        .array_layers = 1,
        .format       = RHI_FORMAT_R8_UNORM,
        .usage        = RHI_TEXTURE_USAGE_SAMPLED | RHI_TEXTURE_USAGE_TRANSFER_DST,
        .memory       = RHI_MEMORY_GPU_ONLY,
        .debug_name   = "imgui_orb_font",
    } );
    if ( !rhi_handle_valid( s_orb.atlas ) ) { free( pixels ); return false; }

    if ( !rhi()->upload_texture( s_orb.atlas, pixels, pixel_count, 0, 0 ) )
    {
        free( pixels );
        rhi()->texture_destroy( s_orb.atlas );
        s_orb.atlas = ( rhi_texture_t ){ 0 };
        return false;
    }
    free( pixels );

    s_orb.atlas_idx = rhi()->register_texture( s_orb.atlas );
    if ( s_orb.atlas_idx == 0 )
    {
        rhi()->texture_destroy( s_orb.atlas );
        s_orb.atlas = ( rhi_texture_t ){ 0 };
        return false;
    }

    s_orb.ascent  = hdr.ascent;
    s_orb.descent = hdr.descent;
    s_orb.atlas_w = hdr.atlas_w;
    s_orb.atlas_h = hdr.atlas_h;
    s_orb.loaded  = true;
    return true;
}

/*----------------------------------------------------------------------------------------------
    font_init / font_shutdown
----------------------------------------------------------------------------------------------*/

static bool
font_init( void )
{
    if ( !atlas_init_one( &s_font_8 ) )
        return false;
    if ( !atlas_init_one( &s_font_12 ) )
    {
        atlas_shutdown_one( &s_font_8 );
        return false;
    }
    font_select( s_layout.font_size );
    return true;
}

static void
font_shutdown( void )
{
    font_unload_orb();
    atlas_shutdown_one( &s_font_12 );
    atlas_shutdown_one( &s_font_8 );
    s_font_active = NULL;
}

/*----------------------------------------------------------------------------------------------
    font_char_w / font_char_h / font_line_h / font_text_w / font_atlas_idx
    Defined before font_glyph() because the bitmap branch of font_glyph() calls font_char_w().
----------------------------------------------------------------------------------------------*/

/* Monospace advance for the bitmap path (also used by font_text_w bitmap branch). */
static f32 font_char_w( void )
{
    const font_def_t* def = s_font_active->def;
    return (f32)s_layout.font_size * (f32)def->glyph_w / (f32)def->glyph_h;
}

/* Pixel height of the text area (used for vertical centering in widgets). */
static f32 font_char_h( void )
{
    if ( s_orb.loaded )
        return (f32)( s_orb.ascent - s_orb.descent );
    return (f32)s_layout.font_size;
}

static f32 font_line_h( void )
{
    if ( s_orb.loaded )
        return (f32)( s_orb.ascent - s_orb.descent );
    u32 lh = s_layout.font_size + s_layout.font_size / 4u;
    return (f32)lh;
}

static f32
font_text_w( const char* str )
{
    f32 w = 0.0f;
    if ( s_orb.loaded )
    {
        for ( ; *str; ++str )
        {
            u8 ch = (u8)*str;
            if ( ch >= 32 && ch <= 126 )
                w += (f32)s_orb.lookup[ ch - 32 ].advance;
        }
    }
    else
    {
        for ( ; *str; ++str )
            w += font_char_w();
    }
    return w;
}

/* Bindless index of the active atlas texture. */
static u32 font_atlas_idx( void )
{
    return s_orb.loaded ? s_orb.atlas_idx : s_font_active->atlas_idx;
}

/*----------------------------------------------------------------------------------------------
    font_glyph -- per-character draw parameters; dispatches on s_orb.loaded.

    Outputs:
        u0..v1   atlas UV rect for the glyph bitmap
        ox, oy   pixel offsets from (cursor_x, text_y) to the top-left of the bitmap
        gw, gh   pixel dimensions of the bitmap to draw (0 for invisible glyphs like space)
        advance  horizontal cursor advance in pixels
----------------------------------------------------------------------------------------------*/

static void
font_glyph( u8 ch,
            f32* u0, f32* v0, f32* u1, f32* v1,
            f32* ox, f32* oy, f32* gw, f32* gh,
            f32* advance )
{
    if ( s_orb.loaded )
    {
        if ( ch < 32 || ch > 126 ) ch = (u8)'?';
        const orb_font_glyph_t* g = &s_orb.lookup[ ch - 32 ];

        f32 iw = 1.0f / (f32)s_orb.atlas_w;
        f32 ih = 1.0f / (f32)s_orb.atlas_h;
        *u0 = (f32)g->atlas_x * iw;
        *v0 = (f32)g->atlas_y * ih;
        *u1 = *u0 + (f32)g->w * iw;
        *v1 = *v0 + (f32)g->h * ih;

        *ox      = (f32)g->bearing_x;
        *oy      = (f32)( s_orb.ascent - (i32)g->bearing_y );
        *gw      = (f32)g->w;
        *gh      = (f32)g->h;
        *advance = (f32)g->advance;
    }
    else
    {
        /* Bitmap path: fixed-grid UV, full-cell draw, monospace advance. */
        if ( ch < 32 || ch > 127 ) ch = (u8)'?';
        const font_def_t* def = s_font_active->def;

        u32 idx = (u32)( ch - 32 );
        u32 col = idx % def->glyphs_row;
        u32 row = idx / def->glyphs_row;

        *u0 = (f32)( col * def->glyph_w ) / (f32)def->atlas_w;
        *v0 = (f32)( row * def->glyph_h ) / (f32)def->atlas_h;
        *u1 = *u0 + (f32)def->glyph_w    / (f32)def->atlas_w;
        *v1 = *v0 + (f32)def->glyph_h    / (f32)def->atlas_h;

        *ox      = 0.0f;
        *oy      = 0.0f;
        *gw      = font_char_w();
        *gh      = (f32)s_layout.font_size;
        *advance = font_char_w();
    }
}

