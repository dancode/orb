/*==============================================================================================
    Includes
==============================================================================================*/

#include "runtime_service/rhi/vk_functions.h"

/*=============================================================================================
    Library loading
==============================================================================================*/

static bool
vk_lib_init()
{
#if !OS_WINDOWS
    return false;
#endif

    if ( g_vk.dll == NULL )
    {
        g_vk.dll = sys_library_load( "vulkan-1.dll" );
    }
    if ( g_vk.dll == NULL )
    {
        LOG_ERROR( "could not load vulkan-1.dll" );
        return false;
    }

    // vk_lib_exported_entry_points();
    // vk_lib_global_entry_points();

    return true; /* success */
}

/*==============================================================================================
    Library unloading
==============================================================================================*/

static void
vk_lib_exit()
{
    if ( g_vk.dll != NULL )
    {
        sys_library_unload( g_vk.dll );
        g_vk.dll = NULL;
    }
}

