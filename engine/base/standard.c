uint32_t
next_pow2_u32( uint32_t v )
{
    // Round up to next power of 2

    if ( v == 0 )
        return 1;
    v--;
    v |= v >> 1;
    v |= v >> 2;
    v |= v >> 4;
    v |= v >> 8;
    v |= v >> 16;
    v++;
    return v;
}

bool
is_power_of_two( uint32_t v )
{
    /* Helper: check power-of-two */
    return v != 0 && ( ( v & ( v - 1 ) ) == 0 );
}

